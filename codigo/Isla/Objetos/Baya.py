from codigo.Isla.Helper import Helper

class Baya:

    def __init__(self):
        self.image = Helper.BAYA_OBJETO
        self.nombre = "Baya"


    def toJson(self):
        return {
            'objeto' : 'Baya',
            'name' : self.nombre,
        }

    def getNombre(self):
        return self.nombre

    def setNombre(self, nombre):
        self.nombre = nombre

    def getImage(self):
        return self.image

    def setImage(self, image):
        self.image = image